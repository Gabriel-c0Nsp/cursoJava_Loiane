package questao04;

public class ConversaoDeUnidadesDeArea {

  public static double calcularMetrosPes(double metroPe) {
    return metroPe * 10.76;
  }

  public static double calcularPesCM(double peCM) {
    return peCM * 929;
  }

  public static double calcularMilhaAcre(double milhaAcre) {
    return milhaAcre * 640;
  }

  public static double calcularAcrePes(double acrePe) {
    return acrePe * 43.560;
  }
}
