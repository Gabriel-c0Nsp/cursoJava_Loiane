package questao05;

public class ConversaoDeUnidadesDeVolume {

  public static double calcularLitrosCMcubicos(double litroCMcubico) {
    return litroCMcubico * 1000;
  }

  public static double calcularMetroscubicosLitros(double metrocubicolitro) {
    return metrocubicolitro * 1000;
  }

  public static double calcularMetroscubicosPescubicos(double metrocubicoPecubico) {
    return metrocubicoPecubico * 35.32;
  }

  public static double calcularGaloesamericanosPolegadasCubicas(double galaoamericanoPolegadacubica) {
    return galaoamericanoPolegadacubica * 231;
  }

  public static double calcularGaloesamericanosLitros(double galaoamericanoLitro) {
    return galaoamericanoLitro * 3785;
  }
}
